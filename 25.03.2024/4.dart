void main(List<String> args) {
  final int num1 = 10;
  const int num2 = 15;

  // Ikki sonni qo'shish
  print("$num1 + $num2 = ${num1 + num2}");

  // Ikki sonni ayirish
  print("$num1 - $num2 = ${num1 - num2}");
  
  // Ikki sonni ko'paytrish
  print("$num1 * $num2 = ${num1 * num2}");

  // Ikki sonni bo'lish
  print("$num1 / $num2 = ${num1 / num2}");
}